    private void buttonSeven2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSeven2ActionPerformed
        if(!respuestajTextField.getText().equalsIgnoreCase("")){
            Usuarios buscar =Controller.UsuarioController.UnicoUsuario(usuariojComboBox.getSelectedItem().toString());
                if(buscar.getRespuesta().equals(respuestajTextField.getText())){
                    llenarprogreso();
                    contraseñapanelShadow.setVisible(true);
                    tabbedPaneHeader1.setSelectedIndex(1);
                    usuariojComboBox.setEnabled(false);
                    respuestajTextField.setEditable(false);
                }else{
                    JOptionPane.showMessageDialog(null, "La contraseña es incorrecta \n"
                            +"Revise nuevamente", "Contraseña invalida",JOptionPane.ERROR_MESSAGE);
                }
        
        
        }
        // TODO add your handling code here:
        
        
    }//GEN-LAST:event_buttonSeven2ActionPerformed

    private void buttonSeven3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSeven3ActionPerformed
        String nueva = new String(nuevajPasswordField.getPassword());
        String confirmar = new String(confirmarjPasswordField.getPassword());
        if(!nueva.equalsIgnoreCase("") && !confirmar.equalsIgnoreCase("")){
            
        
                if(nueva.equals(confirmar))
                {

                    Usuarios buscar =Controller.UsuarioController.UnicoUsuario
                            (usuariojComboBox.getSelectedItem().toString());

                       if(Controller.UsuarioController.UpdateUsuario
                            (buscar.getUsername(), nueva, buscar.getTipo()
                            , buscar.getPreguntaSeguridad(), buscar.getRespuesta()
                            ,buscar.getSucursales())==true)
                       {
                           llenarprogreso();
                           JOptionPane.showMessageDialog(null, "Se ha guardado exitosamente"
                                   ,"Registro guardado", JOptionPane.INFORMATION_MESSAGE);
                           new Login().setVisible(true);
                           dispose();
                       }else{
                           JOptionPane.showMessageDialog(null, "Ha ocurrido un error Intente nuevamente"
                                   ,"Error al guardar", JOptionPane.ERROR_MESSAGE);
                       }



                }else{
                    JOptionPane.showMessageDialog(null, "Las contraseñas no son iguales"
                            ,"Error tienen que ser iguales", JOptionPane.ERROR_MESSAGE);
                }
        }else{
            JOptionPane.showMessageDialog(null, "Error no puedes dejar vacia la contraseña","Sistema Central",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonSeven3ActionPerformed

