    public void llenarprogreso(){
        int selec=tabbedPaneHeader1.getSelectedIndex();
        if (selec==0){
            progressBarRect1.setValue(67);
        }
         if (selec==1){
            progressBarRect1.setValue(100);
        }
    }
    public  void loadUsuarios(){
        DefaultComboBoxModel datos=new DefaultComboBoxModel();
        List<Usuarios> resultado=Controller.UsuarioController.ShowUsuarios();
        /*Se crea un objeto de tipo RecuperacionContraseña para
     * poder acceder a todos los objetos de la clase RecuperacionContraseña
     */  
        for(Usuarios o: resultado){
            DisplayItem d = new DisplayItem(o.getUsername(), o.getClave());
            datos.addElement(d);
        } 
        usuariojComboBox.setModel(datos);
    }
    void Pregunta(){
        if(usuariojComboBox.getSelectedIndex()!=-1){
        Usuarios buscar =Controller.UsuarioController.UnicoUsuario
                (usuariojComboBox.getSelectedItem().toString());
        preguntajTextField.setText(buscar.getPreguntaSeguridad());
        }else{
            JOptionPane.showMessageDialog(null, "Tienes que seleccionar un usuario"
                    ,"Usuarios", JOptionPane.WARNING_MESSAGE);
        }
    }
