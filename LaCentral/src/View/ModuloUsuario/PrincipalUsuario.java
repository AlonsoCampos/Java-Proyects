    private void guardarcontraseñajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarcontraseñajButtonActionPerformed
        String actual = new String(actualjPasswordField.getPassword());
        if(!actual.equalsIgnoreCase(""))
        {
                String nueva = new String(nuevajPasswordField.getPassword())
                ,confirmar = new String(confirmarjPasswordField.getPassword());
              if(!nueva.equalsIgnoreCase("") && !confirmar.equalsIgnoreCase(""))
              {
                        if(busqueda().getPassword().equals(actual))
                        {
                            if(nueva.equals(confirmar))
                            {
                                    if(Controller.UsuarioController.UpdateUsuario
                                            (busqueda().getUsername(), 
                                             nueva,
                                             busqueda().getTipo(), 
                                             busqueda().getPreguntaSeguridad(),
                                             busqueda().getRespuesta(),
                                             busqueda().getSucursales())==true)
                                    {
                                        JOptionPane.showMessageDialog(null, "Contraseña guardada"
                                                          ,"Cambios aplicados", 
                                                          JOptionPane.INFORMATION_MESSAGE);
                                        deshabilitarCambioContraseña();
                                        limpiarPassword();
                                        nocambiarjRadioButton.setSelected(true);
                                        cambiarjRadioButton.setSelected(false);

                                    }
                                    else
                                    {
                                        JOptionPane.showMessageDialog(null, "No se ha podido guardar"
                                                + "\n intente nuevamente"
                                    ,"Error al guardar", JOptionPane.ERROR_MESSAGE); 
                                    }
                            }
                            else
                            {
                              JOptionPane.showMessageDialog(null, "Las contraseñas no son iguales "
                                 ,"Contraseñas diferentes", JOptionPane.ERROR_MESSAGE);  
                            }
                        }
                        else
                        {
                           JOptionPane.showMessageDialog(null, "Error no ha ingresado su "
                                 + "contraseña actual"
                                 ,"Error de Contraseña", JOptionPane.ERROR_MESSAGE);  
                        }
              }
              else
              {
                  JOptionPane.showMessageDialog(null, "No puede dejar campos vacios "
                       ,"Error", JOptionPane.ERROR_MESSAGE);
              }
                
        }
            else
        {
            JOptionPane.showMessageDialog(null, "Ingrese su contaseña "
                     + "actual"
                     ,"Contraseña no Valida", JOptionPane.ERROR_MESSAGE);  
        }
            
    }//GEN-LAST:event_guardarcontraseñajButtonActionPerformed

    private void guardarPreguntajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarPreguntajButtonActionPerformed
        if(preguntajComboBox.getSelectedIndex()!=0)
        {
            if(!respuestajTextField.getText().equalsIgnoreCase(""))
            {
                if(Controller.UsuarioController.UpdateUsuario(
                        busqueda().getUsername(), 
                        busqueda().getPassword(),
                        busqueda().getTipo(),
                        preguntajComboBox.getSelectedItem().toString(), 
                        respuestajTextField.getText(),
                        busqueda().getSucursales())==true)
                {
                    JOptionPane.showMessageDialog(null, "Cambios aplicados"
                     ,"Guardar", JOptionPane.INFORMATION_MESSAGE);
                    limpiarRespuesta();
                    preguntaNojRadioButton.setSelected(true);
                    preguntaSijRadioButton.setSelected(false);
                    guardarPreguntajButton.setVisible(false);
                    deshabilitarCambioPregunta();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Error al guardar intente nuevamente"
                     ,"Intente nuevamente", JOptionPane.ERROR_MESSAGE);    
                }
            }
            else
            {
                    JOptionPane.showMessageDialog(null, "Tienes que ingresar "
                            + "una respuesta "
                     ,"Respuesta invalida", JOptionPane.ERROR_MESSAGE);  
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Tienes que elegir una pregunta "
                     ,"Opcion no valida", JOptionPane.ERROR_MESSAGE);  
        }
    }//GEN-LAST:event_guardarPreguntajButtonActionPerformed

