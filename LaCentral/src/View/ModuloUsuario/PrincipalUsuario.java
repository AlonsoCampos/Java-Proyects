    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String contaseña = new String(contraseñajPasswordField.getPassword());
        if(!usuariojTextField.getText().equalsIgnoreCase("") && !contaseña.equalsIgnoreCase("")){
            
            if(busqueda()!=null)
            {
               if(busqueda().getPassword().equals(contaseña))
               {
                    JOptionPane.showMessageDialog(null, "Acceso Permitido "
                    ,"Permiso Otorgado", JOptionPane.INFORMATION_MESSAGE);
                    cambiarjPanel.setVisible(true);
                    contraseñajPasswordField.setEditable(false);
                    usuariojTextField.setEnabled(false);
               }
               else
               {
                 JOptionPane.showMessageDialog(null, "Verique que "
                    + "que haya escrito bien su contraseña"
                    ,"Contraseña no Valida", JOptionPane.ERROR_MESSAGE);  
               }
                       
            }
            else
            {
                JOptionPane.showMessageDialog(null, "El usuario "+usuariojTextField.getText()
                    + " no existe"+ "\n Verique que "
                    + "que haya escrito bien el usuario"
                    ,"Usuario no encontrado", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

